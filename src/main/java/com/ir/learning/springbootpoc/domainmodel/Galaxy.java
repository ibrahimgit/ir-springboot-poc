package com.ir.learning.springbootpoc.domainmodel;

public class Galaxy {
	
	private String name;
	private Long length;
	private Long breadth;
	private Long distanceFromEarth;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getLength() {
		return length;
	}
	public void setLength(Long length) {
		this.length = length;
	}
	public Long getBreadth() {
		return breadth;
	}
	public void setBreadth(Long breadth) {
		this.breadth = breadth;
	}
	public Long getDistanceFromEarth() {
		return distanceFromEarth;
	}
	public void setDistanceFromEarth(Long distanceFromEarth) {
		this.distanceFromEarth = distanceFromEarth;
	}
	
	

}
